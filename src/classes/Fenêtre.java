    private void btnAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterActionPerformed
        Véhicule véhicule=null;
        
        String marque,modèle,immatriculation,type;
        int nombrePortes,vitesseMax,choix=0;
        boolean remorque;
        type = JOptionPane.showInputDialog(this, "Entrez le type du Véhicule Voiture/Camion/Moto", 
                "Type", JOptionPane.PLAIN_MESSAGE);
        
        if(type==null){
        
        }
        
        else if(type.equalsIgnoreCase("Voiture")){
            marque = JOptionPane.showInputDialog(this, "Entrez la marque du Véhicule", 
                "Marque", JOptionPane.PLAIN_MESSAGE);
            modèle = JOptionPane.showInputDialog(this, "Entrez le modèle du Véhicule", 
                "Modèle", JOptionPane.PLAIN_MESSAGE);
            immatriculation = JOptionPane.showInputDialog(this, "Entrez l'immatriculation du Véhicule", 
                "Immatriculation", JOptionPane.PLAIN_MESSAGE);
            nombrePortes = Integer.parseInt(JOptionPane.showInputDialog(this, "Entrez le nombre de portes", 
                "Nombre de portes", JOptionPane.PLAIN_MESSAGE));
            véhicule = new Voiture(marque,modèle,immatriculation,nombrePortes);  
        }
            
        else if(type.equalsIgnoreCase("Moto")){
            marque = JOptionPane.showInputDialog(this, "Entrez la marque du Véhicule", 
                "Marque", JOptionPane.PLAIN_MESSAGE);
            modèle = JOptionPane.showInputDialog(this, "Entrez le modèle du Véhicule", 
                "Modèle", JOptionPane.PLAIN_MESSAGE);
            immatriculation = JOptionPane.showInputDialog(this, "Entrez l'immatriculation du Véhicule", 
                "Immatriculation", JOptionPane.PLAIN_MESSAGE);
            vitesseMax = Integer.parseInt(JOptionPane.showInputDialog(this, "Entrez la vitesse maximale", 
                "Vitesse", JOptionPane.PLAIN_MESSAGE));
            véhicule = new Moto(marque, modèle, immatriculation, vitesseMax);
        }
            
        else if(type.equalsIgnoreCase("Camion")){
            marque = JOptionPane.showInputDialog(this, "Entrez la marque du Véhicule", 
                "Marque", JOptionPane.PLAIN_MESSAGE);
            modèle = JOptionPane.showInputDialog(this, "Entrez le modèle du Véhicule", 
                "Modèle", JOptionPane.PLAIN_MESSAGE);
            immatriculation = JOptionPane.showInputDialog(this, "Entrez l'immatriculation du Véhicule", 
                "Immatriculation", JOptionPane.PLAIN_MESSAGE);
            choix = JOptionPane.showConfirmDialog(this,"Le camion a-t-il un remorque?","Remorque",JOptionPane.YES_NO_OPTION);
        if(choix==0)remorque=true;
        else remorque=false;
        véhicule = new Camion(marque, modèle, immatriculation, remorque);
        
        }
        else{
            JOptionPane.showConfirmDialog(this, "Votre choix est incorrect", "choix incorrect", JOptionPane.OK_OPTION);
        }
           Véhicules.add(véhicule);
        modèleListe.add(modèleListe.getSize(), véhicule);
        listeVéhicules.setModel(modèleListe);
    }//GEN-LAST:event_btnAjouterActionPerformed

