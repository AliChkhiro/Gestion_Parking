    private void btnModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifierActionPerformed
        String marque,modèle,immatriculation;
        int nombrePortes,remorque,vitesseMax;
         if(listeVéhicules.isSelectionEmpty())
           JOptionPane.showConfirmDialog(this, "Séléctionner un véhiclue à modifier",
                   "Pas de séléction", JOptionPane.ERROR_MESSAGE);
         else{
             int indice = listeVéhicules.getSelectedIndex();
             Véhicule v = Véhicules.get(indice);
             marque = JOptionPane.showInputDialog(this, "Changez la marque",
                     v.getMarque());
             v.setMarque(marque);
             modèle = JOptionPane.showInputDialog(this, "Changez le modèle",
                     v.getModèle());
             v.setModèle(modèle);
             immatriculation = JOptionPane.showInputDialog(this, "Changez l'immatriculation",
                     v.getImmatriculation());
             v.setImmatriculation(immatriculation);
             if(v instanceof Voiture){
                 nombrePortes = Integer.valueOf(JOptionPane.showInputDialog(this, "Changez le nombre de portes", ((Voiture) v).getNombrePortes()));
                 ((Voiture) v).setNombrePortes(nombrePortes);
             }
             else if(v instanceof Camion){
                 remorque = JOptionPane.showConfirmDialog(this, "le camion a-t-il un remorque?", "Remorque", JOptionPane.YES_NO_OPTION);
                 boolean choix = true;
                 if(remorque==JOptionPane.NO_OPTION)
                     choix=false;
                 ((Camion) v).setRemorque(choix);
             }
             else if(v instanceof Moto){
                  vitesseMax = Integer.valueOf(JOptionPane.showInputDialog(this, "Changez la vitesse maximale",
                          ((Moto) v).getVitesseMax()));
                  ((Moto) v).setVitesseMax(vitesseMax);
             }
             else{
                 JOptionPane.showMessageDialog(this, "Erreur Inconnue", "Erreur", JOptionPane.ERROR_MESSAGE);
             }
             listeVéhicules.setModel(modèleListe);
         }
    }//GEN-LAST:event_btnModifierActionPerformed

